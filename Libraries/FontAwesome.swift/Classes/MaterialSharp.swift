import UIKit
import CoreText

private class FontLoader {
    class func loadFont(fileName: String, fileExtension: String) {
        let bundle = Bundle(for: FontLoader.self)
        
        let fontURL = bundle.url(forResource: fileName, withExtension: fileExtension)!
        
        let data = try! Data(contentsOf: fontURL)
        
        let provider = CGDataProvider(data: data as CFData)
        let font = CGFont(provider!)!
        
        var error: Unmanaged<CFError>?
        if !CTFontManagerRegisterGraphicsFont(font, &error) {
            let errorDescription: CFString = CFErrorCopyDescription(error!.takeUnretainedValue())
            let nsError = error!.takeUnretainedValue() as AnyObject as! NSError
            NSException(name: NSExceptionName.internalInconsistencyException, reason: errorDescription as String, userInfo: [NSUnderlyingErrorKey: nsError]).raise()
        }
    }
}

public extension UIFont {
    class func customIcon(of size: CGFloat) -> UIFont {
        let fontFamilyName = "Material Icons Sharp"
        let fontFileName = "MaterialIconsSharp"
        let fontFileExtension = "otf"
        if UIFont.fontNames(forFamilyName: fontFamilyName).isEmpty {
            FontLoader.loadFont(fileName: fontFileName, fileExtension: fontFileExtension)
        }
        
        return UIFont(name: fontFamilyName, size: size)!
    }
}

